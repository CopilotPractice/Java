
public class OpTest02 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 3;
		
		System.out.println(num1 > num2); // true
		System.out.println(num1 < num2); // false
		System.out.println(num1 >= num2); // true
		System.out.println(num1 <= num2); // false
		System.out.println(num1 == num2); // false
		System.out.println(num1 != num2); // true
		
		boolean myBool = num1 > num2;
		System.out.println(myBool);
		
	}

}
